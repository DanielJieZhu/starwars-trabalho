package starwars;

public class Mestre extends Jedi{
    private boolean imortal;
    private String campoVidencia;

    public boolean isImortal() {
		return imortal;
	}
	public void setImortal(boolean imortal) {
		this.imortal = imortal;
	}
	public String getCampoVidencia() {
		return campoVidencia;
	}
	public void setCampoVidencia(String campoVidencia) {
		this.campoVidencia = campoVidencia;
	}
}
